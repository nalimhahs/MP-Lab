.MODEL SMALL
.STACK 100H
.DATA

COUNT EQU 06H

.CODE

MOV AX, 00H
MOV SI, 500H
MOV [SI], AX
INC SI
ADD AX, 01H
MOV [SI], AX
MOV CX, COUNT
SUB CX, 02H

L1:
	MOV AX, [SI-1]
	ADD AX, [SI]
	ADD SI, 01H
	MOV [SI], AX
	LOOP L1

MOV SI, 500H
MOV CX, COUNT

PRINT:
	MOV DX, [SI]
	ADD DX, 30H
	MOV AH, 02H
	INT 21H
	INC SI
	LOOP PRINT

EXIT:     
    MOV AH, 4CH
    INT 21H

END 